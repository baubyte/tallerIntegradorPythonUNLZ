"""
Ejercicio 1.5
Pedirle al usuario que ingrese cuatro números y mostrarle el promedio.
 """
#Variables
numeroUno = int(input("Ingrese un Número: "))
numeroDos = int(input("Ingrese un Número: "))
numeroTres = int(input("Ingrese un Número: "))
numeroCuatro = int(input("Ingrese un Número: "))
promedio = (numeroUno + numeroDos + numeroTres + numeroCuatro) / 4
print("El Promedio de lo Números Ingresados es:", promedio)